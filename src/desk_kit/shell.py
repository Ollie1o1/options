"""The desk shell: one masthead, one grid, one stylesheet, one script.

Every HTML report composes these pure builders. The shell owns everything that
must feel identical across pages — chrome, typography, cards, badges, tabs,
anchor nav, theme flip — so a report module only writes panel content.

Theme: dark ("terminal") is the default applied theme; the choice persists in
localStorage under ONE key ('desk-theme') so it follows the reader across
every desk page. Charts re-ink via CSS custom properties, no re-render.
"""
import html as _html

from src.desk_kit import theme


def esc(v) -> str:
    return _html.escape("" if v is None else str(v), quote=True)


def num(v, fmt="{:,.2f}", dash="—"):
    try:
        f = float(v)
        if f != f:
            return dash
        return fmt.format(f)
    except (TypeError, ValueError):
        return dash


# ── Components ───────────────────────────────────────────────────────────────

def card(title, body, span=6, extra_cls="", anchor=None):
    aid = ' id="{}"'.format(esc(anchor)) if anchor else ""
    return ('<section class="card c{s}{x}"{a}><h5>{t}</h5>{b}</section>'
            .format(s=span, x=(" " + extra_cls if extra_cls else ""), a=aid,
                    t=esc(title), b=body))


_TONES = {"good": " g", "bad": " b", "warn": " w", "": ""}


def kpi(label, value, sub="", tone="", span=3):
    """Stat tile. The value is proportional-figure (display size), not
    tabular — tabular digits read loose at 24px."""
    return ('<div class="card kpi c{sp}"><div class="eye">{l}</div>'
            '<div class="kv{t}">{v}</div><div class="ks">{s}</div></div>'
            .format(sp=span, l=esc(label), t=_TONES.get(tone, ""), v=value,
                    s=sub))


def strip(cells):
    """Dense KPI strip: cells = [(label, value_html, tone)]."""
    inner = "".join(
        '<div><div class="eye">{l}</div><div class="sv{t}">{v}</div></div>'
        .format(l=esc(l), t=_TONES.get(tone, ""), v=v)
        for l, v, tone in cells)
    return ('<div class="strip" style="grid-template-columns:repeat({n},'
            'minmax(0,1fr))">{c}</div>').format(n=max(1, len(cells)), c=inner)


def badge(text, kind="warn"):
    return '<span class="badge k-{k}">{t}</span>'.format(k=esc(kind),
                                                         t=esc(text))


def verdict_pill(decision, cls_map=None):
    cls_map = cls_map or {}
    cls = cls_map.get(str(decision).upper(), "v-neutral")
    return '<span class="verdict {c}">{d}</span>'.format(c=cls,
                                                         d=esc(decision))


def rows_table(pairs):
    """Two-column label/value table; values right-aligned mono."""
    return "<table>" + "".join(
        '<tr><td>{}</td><td class="n">{}</td></tr>'.format(esc(k), v)
        for k, v in pairs) + "</table>"


def ph(text):
    return '<div class="ph">{}</div>'.format(text)


def grid(cells):
    return '<div class="grid">' + "".join(cells) + "</div>"


