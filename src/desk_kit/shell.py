def page(title, mast_html, body_html, extra_css="", extra_js=""):
    return (
        '<!DOCTYPE html>\n<html lang="en"><head><meta charset="utf-8">'
        '<meta name="viewport" content="width=device-width,initial-scale=1">'
        "<title>{title}</title><style>{tokens}{css}{extra}</style></head>"
        '<body>{mast}<main class="desk">{body}</main>'
        '<div class="tip" id="tip"></div>'
        "<script>{js}{xjs}</script></body></html>"
    ).format(title=esc(title), tokens=theme.css_tokens(), css=_CSS,
             extra=extra_css, mast=mast_html, body=body_html, js=_JS,
             xjs=extra_js)


