def tabbar(tabs):
    """tabs = [(tab_id, label)] → one shell tab group (shortcut keys 1-9)."""
    return '<nav class="tabbar" data-tabgroup>' + "".join(
        '<button class="tabbtn" data-tab="{t}">{n}&nbsp;{l}</button>'.format(
            t=esc(tid), n=i + 1, l=label)
        for i, (tid, label) in enumerate(tabs)) + "</nav>"


def anchor_nav(anchors):
    """anchors = [(section_id, label)] → sticky in-page nav with scrollspy."""
    return '<nav class="tabbar" data-spynav>' + "".join(
        '<a class="tabbtn" data-spy="{a}" href="#{a}">{l}</a>'.format(
            a=esc(aid), l=esc(label))
        for aid, label in anchors) + "</nav>"


def masthead(doc_kind, title_html, meta_html="", nav_html="", where=None):
    """The shared chrome. `where` picks the cross-link set (output dir name)."""
    links = DESK_LINKS.get(where or "", ())
    linkbar = "".join('<a class="desklink" href="{h}">{l}</a>'.format(
        h=esc(h), l=esc(l)) for l, h in links)
    return (
        '<header class="mast"><div class="mastrow">'
        '<div class="mastid"><span class="wordmark">OPTIONS DESK</span>'
        '<span class="dockind">{k}</span>{t}</div>'
        '<div class="mastmeta">{links}{meta}'
        '<button class="toggle" onclick="flipTheme()">'
        '<span id="tglabel">◑ Light</span></button></div></div>'
        "{nav}</header>"
    ).format(k=esc(doc_kind), t=title_html, links=linkbar, meta=meta_html,
             nav=nav_html)


def chipline(items):
    """Masthead meta items: [(label, value_html)] → eye-label chips."""
    return "".join('<span class="eye m">{l} {v}</span>'.format(
        l=esc(l), v=v) for l, v in items)


